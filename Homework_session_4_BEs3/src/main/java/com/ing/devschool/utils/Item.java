package com.ing.devschool.utils;

public class Item {

    private String name;
    private int contor;

    public Item(String name, int contor) {
        this.name = name;
        this.contor = contor;
    }

    public String getName() {
        return name;
    }


    public int getContor() {
        return contor;
    }
}
